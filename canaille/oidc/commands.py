@jwk.command()
@click.argument("kid", required=False)
@click.option(
    "--format",
    "output_format",
    type=click.Choice(["json", "pem"], case_sensitive=False),
    default="json",
    help="Output the key metadata as JSON or the raw public keys as PEM.",
)
@click.option(
    "--active-only",
    is_flag=True,
    default=False,
    help="Only export the active keys instead of the active and inactive ones.",
)
@with_appcontext
def export(kid: str | None, output_format: str, active_only: bool):
    """Export the public part of the server signing keys.

    Without argument, every asymmetric verification key is exported, active and
    inactive by default so tokens signed during a key rotation window can still
    be verified. A single key can be selected by passing its KID.

    The ``json`` format (default) emits the key metadata: kid, signing
    algorithm, usage and key type. The ``pem`` format emits the raw public keys
    in PEM (SubjectPublicKeyInfo) format, suitable for external JWT validators
    that verify Canaille-issued tokens locally without fetching the JWKS
    endpoint.

    Symmetric keys have no public part and are skipped; the private key material
    is never emitted.
    """
    if current_app.extensions.get("canaille_oidc_jwks_autogenerated"):
        raise click.ClickException(
            "ACTIVE_JWKS is not configured. Refusing to export the auto-generated "
            "keys as they change on every restart. Please configure ACTIVE_JWKS first."
        )

    keys = server_jwks(include_inactive=not active_only).keys

    if kid is not None:
        selected = next((key for key in keys if key.kid == kid), None)
        if selected is None:
            raise click.ClickException(f"No key found with kid '{kid}'.")
        if isinstance(selected, OctKey):
            raise click.ClickException(
                f"Key '{kid}' is symmetric and has no public part."
            )
        exported = [selected]

    else:
        exported = []
        for key in keys:
            if isinstance(key, OctKey):
                click.echo(
                    f"Skipping symmetric key {key.kid} as it has no public part.",
                    err=True,
                )
                continue
            exported.append(key)

    exported.sort(key=lambda key: key.kid or "")

    if output_format == "pem":
        click.echo(b"".join(key.as_pem(private=False) for key in exported), nl=False)
        return

    entries = [
        {
            "kid": key.kid,
            "alg": get_alg_for_key(key),
            "use": key.as_dict(private=False).get("use", "sig"),
            "kty": key.key_type,
        }
        for key in exported
    ]
    payload = entries[0] if kid is not None else entries
    click.echo(json.dumps(payload, indent=2, sort_keys=True))


@click.group()
def jwt():
    """Client registration management."""
    pass


@jwt.command()
@click.option("--lifetime", default=86400, type=int, help="Token lifetime in seconds")
@with_appcontext
def registration(lifetime: int):
    """Generate a JWT token for dynamic client registration.

    Creates a JWT token with an auto-generated client_id that can be used
    to register a new OAuth2 client.
    """
    if not current_app.config["SERVER_NAME"]:
        raise click.ClickException(
            "You must define SERVER_NAME to issue tokens from the CLI"
        )

    token = build_client_management_token(
        "client:register", timedelta(seconds=lifetime)
    )
    click.echo(token)


@jwt.command()
@click.argument("client-id", required=True)
@click.option("--lifetime", default=86400, type=int, help="Token lifetime in seconds")
@with_appcontext
def management(client_id: str, lifetime: int):
    """Generate a JWT token for client management.

    Creates a JWT token that can be used to manage an existing OAuth2 client.
    """
    if not current_app.config["SERVER_NAME"]:
        raise click.ClickException(
            "You must define SERVER_NAME to issue tokens from the CLI"
        )

    token = build_client_management_token(
        "client:manage", timedelta(seconds=lifetime), client_id
    )
    click.echo(token)


def register(cli):
    cli.add_command(clean)
    cli.add_command(jwk)
    cli.add_command(jwt)
